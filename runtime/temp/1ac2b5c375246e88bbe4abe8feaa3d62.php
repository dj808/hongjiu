1紧跟学科发展前沿，培养应用型人才</br>

机械工程学科实力是衡量一个国家机械装备发展水平的重要标志。在国家“十二五”规划中明确列出大型机械装备为我国的重点发展项目，国家急需机电复合型专业技术人才。本专业所在学科融机械学、控制理论、机器人技术、电子与计算机测控技术、可靠性技术等学科于一体，形成了以大型装备集成技术相关理论研究为特色、机电液相综合的多个研究方向，学科领域广阔。学科培养具有理论基础扎实，工程实践能力强的从事机械产品和系统的研究、设计与制造专门技术人才。</br></br>

2引进和培养相结合，建设一支优秀教学团队</br>

学校在现有教师队伍基础上，围绕专业核心课程群，加强培养引进教师力度，引进高水平学术带头人，吸引高层次人才充实团队实力。在更高的层次上加强师资队伍建设，逐步形成一支结构合理、学术造诣高、教学经验丰富、教学效果好、创新能力强、团结协作精干的师资队伍。

