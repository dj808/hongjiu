大力加强实践教学，切实提高大学生的实践能力，强化实践育人的意识，根据学科对实践教学的要求，结合成人高等教育的需求，合理制定实践教学方案，完善实践教学体系。在实验教学中，不断改革教学内容、方法和手段，完善人才培养模式，从进行教育创新的理念出发，在大学科、淡化专业和模块化教育的大背景下，开设了综合实践课，将过去单一的专业实验教学拓展到多元化多学科的实验教学改革，达到向学生提供一个良好的科技创新教育环境的目的，使学生都能充分发挥自身潜能，激发学习的主动性。学生能结合自身工作了解与本学科相关的交叉学科和边缘学科的知识，加大学科知识的融合，加大相关学科的信息量，开阔学生的视野。增强学生的科技信息量，从而为创新人才的培养创造一个良好的教育环境。</br></br>

5加强实训环节，提高学生综合应用能力</br>

