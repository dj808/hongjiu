同时成立以老带新的成教教学团队，加强对成人教学的研究，提高成人高等教育人才培养质量。</br></br>

3改革教学理念，更新课程体系和课程内容以适应成人高等教育的发展</br>

以人为本，以社会需要为导向，根据人才市场的需求及科学技术的发展，制定符合成人高等教育观点和教学方法，拓宽基础，扩大专业面向，加强实践学时，对课程体系和教育内容进行的改革。

为适应成人和企业需求的这种形势，本专业组成了以长期从事成人教学一线的教授带领年轻教师开展教学研究，加强基础教育，拓宽专业口径，增加实践学时，优化重组基础课，优化专业方向课程体系，采用灵活的考核方式。加强实践教学，培养学生创新精神和实践能力，包括课程设计、生产实习、大作业、综合实验、毕业设计、毕业论文创作、结合工作岗位参与产学工作等。改变传统教学模式，进行教学方法和手段的改革。促进教学活动从以教师为中心，向以学生为主体的转化。

尝试将校企结合的案例教学手段应用到课堂教学与实验改革中，鼓励采取启发式、探究式、讨论课、翻转课堂等教学方式，引导学生主动质疑、探究问题，培养学生批判性思维，以提升课堂教学效果；改革课程考核方式，鼓励强化过程考核，逐步提高平时成绩占比。由任课教师随堂组织阶段性测试，并以利于改进后续学习的方式向学生反馈测试结果。</br></br>

